class NiiTCode {
    private int code;

    public NiiTCode(int code) {
        this.code = code;
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    @Override
    public String toString() {
        return "NiiTCode{" + "code=" + code + '}';
    }

   
 
    
}
